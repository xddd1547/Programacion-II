    private void botonListaPrestamosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonListaPrestamosActionPerformed
      
            StringBuilder sb = new StringBuilder();
    sb.append("=== LISTA DE PRESTAMOS ACTIVOS ===\n\n");
    
    List<Prestamo> prestamos = biblioteca.getPrestamos();
    if (prestamos.isEmpty()) {
        sb.append("(No hay prestamos activos)");
    } else {
        for (Prestamo p : prestamos) {
            sb.append("- Libro: ").append(p.libro.getTitulo())
              .append(" | Estudiante: ").append(p.estudiante.getNombre())
              .append(" | ").append(p.fechaP).append(" â†’ ").append(p.fechaDe).append("\n");
        }
    }
    
    JOptionPane.showMessageDialog(this, sb.toString(), "Lista de Prestamos", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_botonListaPrestamosActionPerformed

    private void botonListaAutoresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonListaAutoresActionPerformed
        
            StringBuilder sb = new StringBuilder();
    sb.append("=== LISTA DE AUTORES ===\n\n");
    
    List<Autor> autores = biblioteca.getAutores();
    if (autores.isEmpty()) {
        sb.append("(No hay autores registrados)");
    } else {
        for (Autor a : autores) {
            sb.append("- ").append(a.getNombre()).append(" (").append(a.getNacionalidad()).append(")\n");
        }
    }
    
    JOptionPane.showMessageDialog(this, sb.toString(), "Lista de Autores", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_botonListaAutoresActionPerformed

