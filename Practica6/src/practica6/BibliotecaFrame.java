    private void botonListaLibrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonListaLibrosActionPerformed
        new LibroFrame(biblioteca).setVisible(true);
        
            StringBuilder sb = new StringBuilder();
    sb.append("=== LISTA DE LIBROS ===\n\n");
    
    List<Libro> libros = biblioteca.getLibros();
    if (libros.isEmpty()) {
        sb.append("(No hay libros registrados)");
    } else {
        for (Libro l : libros) {
            sb.append("- ").append(l.getTitulo()).append(" (ISBN: ").append(l.getIsbn()).append(")\n");
        }
    }
    
    JOptionPane.showMessageDialog(this, sb.toString(), "Lista de Libros", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_botonListaLibrosActionPerformed

